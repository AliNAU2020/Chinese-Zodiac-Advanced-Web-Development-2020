Dynamic web content is a website content that is generated during page loading from various sources. The sources include:

<ul>
	<li>Session Variables </li>
	<li>Database Lookups </li>
	<li>Cookies</li>
</ul>

Unlike dynamic web content, static web content is a website content written in HTML language and stored in web server.The peculiar thing in static web page is written in the HTML language, while 
dynamic web content uses PHP,AJAX,ASP.Net.

A database is used in dynamic web content.In order to utilize dynamic content, you have to make use of dynamic languages like (PHP,JavaScript, ASP.NET) to fetch and load content.